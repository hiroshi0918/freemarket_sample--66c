$(function(){
  $('.item-price').on('input', function(){   //リアルタイムで表示したいのでinputを使う｡入力の度にイベントが発火するようになる｡
    var data = $('.item-price').val(); // val()でフォームのvalueを取得(数値)
    var profit = Math.round(data * 0.9)  // 手数料計算を行う｡dataにかけているのが0.9なのは単に引きたい手数料が10%のため｡
    var fee = (data - profit) // 入力した数値から計算結果(profit)を引く｡それが手数料となる｡
    $('.sell__fee--price').html(fee)
    $('.sell__fee--price').prepend('¥')
    $('.sell__profit--price').html(profit)
    $('.sell__profit--price').prepend('¥')
    $('#price').val(profit) // 計算結果を格納用フォームに追加｡もし､入力値を追加したいのなら､今回はdataを引数に持たせる｡
    if(profit == '') {
    $('.sell__profit--price').html('');
    $('.sell__fee--price').html('');
    }
  })
})